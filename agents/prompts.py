# 페이즈 가이드 — 현재 턴에 해당하는 블록만 ATTACKER_SYSTEM 에 동적 주입.
# 역할 정의·자기 인식 차단(=정체성) 은 ATTACKER_SYSTEM 에 고정돼 바뀌지 않고,
