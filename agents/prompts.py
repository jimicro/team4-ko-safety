"""System prompts for Attacker / Defender / Turn-Evaluator / Final-Evaluator.

Design notes
------------
* Attacker is told the *target_content* (the harmful info we want Defender to
  emit) and the category strategy. It must NOT send the seed verbatim — it
  builds up multi-turn.
* Defender is given NO seed and NO target_content. It only sees the running
  conversation, exactly as a real chatbot user would interact.
* Evaluator (Turn) judges whether THIS turn's Defender reply leaked harmful
  info. Evaluator (Final) summarizes the whole round and produces feedback.
* All evaluators output JSON only (parsed by the node), to make scoring
  deterministic.
"""

